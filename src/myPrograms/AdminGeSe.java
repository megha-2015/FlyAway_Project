package myPrograms;

public class AdminGeSe {
	String a_uname;
	String a_pwd;
	
	public AdminGeSe(String a_uname, String a_pwd){
		this.a_uname = a_uname;
		this.a_pwd = a_pwd;
		
	}
	
	
	public String getA_uname() {
		return a_uname;
	}
	public void setA_uname(String a_uname) {
		this.a_uname = a_uname;
	}
	public String getA_pwdl() {
		return a_pwd;
	}
	public void setA_pwdl(String a_pwdl) {
		this.a_pwd = a_pwdl;
	}
	
}
